	         				<li>延遲出貨累計達 2 檔時，甲方可單方立即終止甲乙雙方合約，並於甲方平台公告為無誠信商家。</li>
	         				<li>若刊登期內均無出貨給甲方寄艙，以違約論，並得向乙方主張原期待利益之拆分及依違約條款規定辦理。</li>
	         				<li>如因延遲出貨導致行政主管機關罰鍰及其他衍生費用等，均由乙方負擔。</li>
