				    <li>無差別待遇：乙方保證供貨予甲方的體驗商品(或服務)內容條件須為正品且不得劣於乙方供貨予其他通路，且對甲方會員妥善提供服務，無任何差別待遇。</li>
				    <li>無損害保證：乙方保證商品或服務不得對甲方會員產生損害，若有損害由乙方全部負責，甲方無需負責。 </li>
				    <li>乙方需負責本次體驗廣告提供之體驗商品(或服務)品質安全，若因體驗商品(或服務)之品質瑕疵而產生之糾紛，需由乙方負責排除。 </li>
				    <li>甲方負責體驗會員申訴事宜，乙方負責相關出貨及符合法令規定之退換貨事宜(經甲方通知後7個工作日內，應至體驗會員指定處所收回商品，逾期未收回視同乙方同意拋棄該項商品所有權)。 </li>
				    <li>乙方保證所取得體驗會員資訊，僅為本合約目的必要之使用，乙方需嚴格遵守個資法各項規定。如無可歸責甲方之事由，乙方如有因違反個資法不慎造成甲方被訴，乙方應對消費
				        者之求償數額進行全部賠償。</li>
				    <li>體驗爭議之處理：若因乙方所提供商品或服務所生之體驗爭議，均會直接影響乙方商譽，請乙方應配合甲方處理至與體驗會員達成協議止，其爭議於與體驗會員達成協議後由乙方負責。如遇有體驗會員爭議時，乙方應信任甲方將考量相關法律規定、雙方 利益及有利於體驗會員之解釋做最佳平衡處理及決定。 </li>
				</ol>
