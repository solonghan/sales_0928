							<li>乙方如使用 E-MAIL 為退換貨通知方式，應每日定時檢查，並於接獲消費者郵件時 48 小時內回覆，若因乙方未立即處理導致消費爭議，所生之損害賠償應由乙方負責。</li>
						</ul>
					</li>
				</ol>
