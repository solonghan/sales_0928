					<li>乙方提供之體驗商品(或服務)或內容如有違法或侵權之虞時，甲方得停止本次體驗行銷，且因乙方所致遭其他合法權利人主張侵權、賠償或訴訟時，應由乙方代位出面賠償，直接對原權利人進行損賠及妥適處理。 </li>
					<li>乙方提供之素材、圖片、內容或服務如有違法或侵權、違反公序良俗或違反甲方廣告刊登政策之虞時，甲方得拒絕刊登該廣告、或移除、停止刊登該訊息內容。</li>
