	    			<li>乙方若無於刊登期結束日內將體驗商品(或服務)正品或兌換憑證寄給甲方(生鮮食品、有保存時效限制或需特殊保存方式之體驗商品為結束日起算7天內)，甲方因需對體驗會員履約，需以原市售價購買對應之商品提供予甲方會員，甲方得向乙方請求本次合約商品定價總金額100％懲罰性違約金，並加收該延遲出貨金額之50％的代為執行手續費，同時於甲方平台公告為無誠信商家。</li>
