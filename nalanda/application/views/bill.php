	const canvas = document.getElementById('canvas');
	const ctx = canvas.getContext('2d');
	var signature = document.getElementById('signature');
	canvas.setAttribute('width', $("#signature").width());
	canvas.setAttribute('height', $("#signature").height());

	//起始點座標
	let x1= 0;
	let y1= 0;

	// 終點座標
	let x2= 0;
	let y2= 0;

	const hasTouchEvent = 'ontouchstart' in window ? true : false;
	const downEvent = hasTouchEvent ? 'touchstart' : 'mousedown';
	const moveEvent = hasTouchEvent ? 'touchmove' : 'mousemove';
	const upEvent = hasTouchEvent ? 'touchend' : 'mouseup';

	let isMouseActive = false;

	canvas.addEventListener(downEvent, function(e){
	  isMouseActive = true  ;
	  x1 = (e.offsetX)?e.offsetX:(e.touches[0].pageX - $("#canvas").offset().left);
	  y1 = (e.offsetY)?e.offsetY:(e.touches[0].pageY - $("#canvas").offset().top);
	  ctx.lineWidth = 3;
	  ctx.lineCap = 'round';
	  ctx.lineJoin = 'round';
	})

	canvas.addEventListener(moveEvent, function(e){
	      if(!isMouseActive) return;
	      
	      x2 = (e.offsetX)?e.offsetX:(e.touches[0].pageX - $("#canvas").offset().left);
	      y2 = (e.offsetY)?e.offsetY:(e.touches[0].pageY - $("#canvas").offset().top);
	      
	      ctx.beginPath();
	      ctx.moveTo(x1, y1);
	      ctx.lineTo(x2, y2);
	      ctx.stroke();
	      ctx.closePath();
	      
	      x1 = x2;
	      y1 = y2;
	})

	canvas.addEventListener(upEvent, function(e){
	  isMouseActive = false;
	})

	window.blockMenuHeaderScroll = false;
	$(window).on('touchstart', function(e){
	    if ($(e.target).closest('#canvas').length == 1) blockMenuHeaderScroll = true;
	});

	$(window).on('touchend', function(){
	    blockMenuHeaderScroll = false;
	});
	// $(window).on('touchmove', function(e){
