	    			<li>乙方提供體驗商品數量時請額外增加一份，以利甲方廣告行銷運用(直接拆封體驗拍照)。</li>
	    			<li>甲方系統提供對應體驗會員於體驗後發佈體驗文機制，刊登期結束日起算30日內需發佈該體驗商品(或服務)的體驗文一則，否則無法參加往後的體驗行銷活動，乙方可將體驗文轉載自乙方商品頁作為體驗評價分享。 </li>
